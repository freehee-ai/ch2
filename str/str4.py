# replace : 문자열 교체 함수

print('banana'.replace('a','o'))
#  old 기존 문자 / new 새로운 문자 의미

print('010-1234-5678'.replace('-', ':'))
print('010-1234-5678'.replace('-', ''))

# repr : 따옴표를 표시하는 함수
print(repr('hello'))

# 공백 제거 함수들
print(repr(' hello world '))
print(repr(' hello world '.strip()))

# 첫글자만 대문자로 바꾸는 함수
print(repr(' hello world '.title()))